package org.bharath.problemsolving.qspidersjavaproblems;

public class SwitchCase {

	public static void main(String[] args) 
	{
		String s = "Rakesh";
		
		switch(s)
		{
		case "Bharath":System.out.println("I am inside bharath");
		break;
		case "Rakesh":System.out.println("I am inside rakesh");
		break;
		default:System.out.println("Inside default");
		}
	}

}
